    private void imprimirConsignaciones() {
        areaConsignaciones.append(fechaOperacion.getText()+" +"+ valorOperacion.getText()+"\n" );
    }
    private void imprimirSaldo(String fecha) {
        areaSaldo.append(fecha+ " = " +String.valueOf(cuentaDeBanco.getSaldo())+"\n");
    }
    private void imprimirRetiros() {
        areaRetiros.append(fechaOperacion.getText()+" -"+ valorOperacion.getText()+"\n" );
    }
    private void borrarTextos(javax.swing.JPanel panel) {
        
        for (int i = 0; i < panel.getComponentCount() ; i++) {
            javax.swing.JTextField campoABorrar;
            if (panel.getComponent(i) instanceof javax.swing.JTextField) { 
               campoABorrar = (javax.swing.JTextField) panel.getComponent(i);
               campoABorrar.setText(null);
            }
        }
    }
