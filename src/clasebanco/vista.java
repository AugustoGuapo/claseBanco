    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cuentaDeBanco.setNombre(nombreCuenta.getText());
        cuentaDeBanco.setFechaCreacion(fechaCuenta.getText());
        cuentaDeBanco.setNumeroTelefono(numeroTelefonoCuenta.getText());
        cuentaDeBanco.setSexo(grupoSexo.getSelection().getActionCommand());
        cuentaDeBanco.setNumeroDeCuenta(numeroCuenta.getText());
        cuentaDeBanco.setTipoDeCuenta(grupoTipoCuentas.getSelection().getActionCommand());
        cuentaDeBanco.setSaldo(0);
        imprimirSaldo(cuentaDeBanco.getFechaCreacion());
        borrarTextos(seccionCrearCuenta);
        grupoSexo.clearSelection();
        grupoTipoCuentas.clearSelection();
        JOptionPane.showMessageDialog(null, "Su cuenta ha sido creada correctamente");

    }//GEN-LAST:event_jButton1ActionPerformed

