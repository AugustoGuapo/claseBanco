    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (cuentaDeBanco.getNumeroDeCuenta().equals(cuentaAUsar.getText())){
            cuentaDeBanco.setSaldo(Integer.parseInt(valorOperacion.getText()));
            imprimirConsignaciones();
            imprimirSaldo(fechaOperacion.getText());
            borrarTextos(seccionOperaciones);
        }
        else
            JOptionPane.showMessageDialog(null, "Introduzca un nÃºmero de cuenta correcto");
        
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (cuentaDeBanco.getNumeroDeCuenta().equals(cuentaAUsar.getText()))
            if (cuentaDeBanco.getSaldo()>=Integer.parseInt(valorOperacion.getText())){
            cuentaDeBanco.setSaldo(Integer.parseInt(valorOperacion.getText())*-1);
            imprimirRetiros();
            imprimirSaldo(fechaOperacion.getText());
            borrarTextos(seccionOperaciones);
            }
            else
                JOptionPane.showMessageDialog(null, "No puede retirar un monto mayor a su saldo");
        else
            JOptionPane.showMessageDialog(null, "Introduzca un nÃºmero de cuenta correcto");
    }//GEN-LAST:event_jButton3ActionPerformed

